package beans;

public class Customerpojo {
private int id;
private String pwd;
private String cid;
private int status;

public Customerpojo(){
}

public int getId()
{
	return id;
}
public void setId(int id) {
	this.id = id;
}
public String getPwd() {
	return pwd;
}
public void setPwd(String pwd) {
	this.pwd = pwd;
}
public String getCid()
{
	return cid;
}
public void setCid(String cid) {
	this.cid = cid;
}
public int getStatus() {
	return status;
}
public void setStatus(int status) {
	this.status = status;
}
	
}	
	

	
	
